def adjust_score_by_source(score: float, source: dict):

    if not source:
        return score

    # 🔥 USAR trust_level (tu formato actual)
    trust = source.get("trust_level", 0.5)

    # --------------------------------------------------
    # 🟢 MUY CONFIABLE
    # --------------------------------------------------
    if trust >= 0.8:
        score *= 0.4

    # --------------------------------------------------
    # 🟡 MEDIA
    # --------------------------------------------------
    elif trust >= 0.6:
        score *= 0.6

    # --------------------------------------------------
    # ⚪ NEUTRA
    # --------------------------------------------------
    elif trust >= 0.4:
        score *= 0.85

    # --------------------------------------------------
    # 🔴 BAJA
    # --------------------------------------------------
    else:
        score *= 1.2

    return max(0.0, min(score, 1.0))